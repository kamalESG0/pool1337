#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	i;
	int	b;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 0 && nb < 10)
	{
		i = nb + '0';
		write(1, &i, 1);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		i = nb % 10;
		b = i + '0';
		write(1, &b, 1);
	}
}
